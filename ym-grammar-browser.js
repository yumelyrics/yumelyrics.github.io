  function isParticleContext(text, i, ch) {
    const prev = text[i - 1] || '';
    const next = text[i + 1] || '';
    if (ch === 'は' && /[わハは]$/.test(text.slice(Math.max(0, i - 4), i))) return false;
    if (ch === 'が' && SKIP_PARTICLE_IN.test(prev)) return false;
    if (ch === 'の' && /[0-9０-９]/.test(next)) return false;
    if (ch.length > 1) return true;
    if (/[\u3040-\u30FF\u4E00-\u9FFF]/.test(prev) || /[\u3040-\u30FF\u4E00-\u9FFF]/.test(next)) return true;
    return 'はをにでのとがの'.includes(ch);
  }

  function collectLevels(items) {
    const set = new Set();
    items.forEach((x) => { if (x.level) set.add(x.level); });
    return ['N1', 'N2', 'N3', 'N4', 'N5'].filter((l) => set.has(l));
  }

  function buildSummary(items) {
    const total = items.length;
    const levels = collectLevels(items);
    const lv = levels.length ? levels.join(' · ') : '—';
    const nPart = items.filter((x) => x.kind === 'partikel').length;
    const nPola = items.filter((x) => x.kind !== 'partikel').length;
    if (!total) return lv + ' · Tidak ada bunpou umum terdeteksi di baris ini.';
    if (total >= 8) return lv + ' · ' + total + ' item bunpou (partikel + pola).';
    if (nPart && nPola) return lv + ' · ' + nPart + ' partikel · ' + nPola + ' pola / bentuk lain.';
    if (nPart) return lv + ' · ' + nPart + ' partikel.';
    return lv + ' · ' + nPola + ' pola / bentuk tata bahasa.';
  }

  function groupByKind(items) {
    const groups = {};
    KIND_ORDER.forEach((k) => { groups[k] = []; });
    items.forEach((it) => {
      if (!groups[it.kind]) groups[it.kind] = [];
      groups[it.kind].push(it);
    });
    return groups;
  }

  function analyzeJapaneseGrammar(jp) {
    if (!jp || !String(jp).trim()) {
      return { summary: 'Baris kosong.', particles: [], phrases: [], items: [], levels: [], groups: {} };
    }
    const text = String(jp).trim();
    const items = [];
    const usedSpan = new Set();

    for (const [ph, label, desc, gloss, level, kind] of PHRASES) {
      let start = 0;
      while (start < text.length) {
        const ix = text.indexOf(ph, start);
        if (ix < 0) break;
        const key = ix + ':' + ph;
        if (!usedSpan.has(key)) {
          usedSpan.add(key);
          for (let k = ix; k < ix + ph.length; k++) usedSpan.add('c' + k);
          items.push(makeItem(ph, label, desc, level, kind, gloss));
        }
        start = ix + ph.length;
      }
    }

    const chars = [...text];
    for (let i = 0; i < chars.length; i++) {
      if (usedSpan.has('c' + i)) continue;
      const ch = chars[i];
      const meta = PARTICLES[ch];
      if (!meta || !isParticleContext(text, i, ch)) continue;
      items.push(makeItem(ch, meta[0], meta[1], meta[3], 'partikel', meta[2]));
    }

    items.sort((a, b) => {
      const ko = KIND_ORDER.indexOf(a.kind) - KIND_ORDER.indexOf(b.kind);
      if (ko !== 0) return ko;
      return (LEVEL_ORDER[a.level] || 9) - (LEVEL_ORDER[b.level] || 9);
    });

    const levels = collectLevels(items);
    const groups = groupByKind(items);
    const particles = items.filter((x) => x.kind === 'partikel').map((x) => ({
      char: x.text, label: x.label, desc: x.desc, level: x.level, glossSlug: x.glossSlug,
      kind: x.kind, kindLabel: x.kindLabel,
    }));
    const phrases = items.filter((x) => x.kind !== 'partikel').map((x) => ({
      text: x.text, label: x.label, desc: x.desc, level: x.level, glossSlug: x.glossSlug,
      kind: x.kind, kindLabel: x.kindLabel,
    }));

    return {
      summary: buildSummary(items),
      items,
      groups,
      particles,
      phrases,
      levels,
      topLevel: levels[0] || null,
    };
  }

