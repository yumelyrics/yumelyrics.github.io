    if (/^(とても|もう|まだ|いつも|よく|すごく|ちょっと|あまり|全然|ぜひ|きっと|多分|必ず|本当|大丈夫|無理|みんな|一緒に|自分で|もう一度|だんだん|どんどん|結局|実は|本当は|やっぱり|もちろん|さすが|意外と|ついに|いきなり|突然|少しずつ|ますます|さらに|幸せ|夢|心|気持ち|本気|約束|人生|世界)$/.test(m)) return 'ekspresi';
